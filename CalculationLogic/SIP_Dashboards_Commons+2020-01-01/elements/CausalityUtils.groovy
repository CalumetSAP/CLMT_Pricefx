/**
 * Retrieves the join fields for all Causality dashboards based on the provided product/customer aggregations.
 * If both aggregations are present then tables are joined on both fields, if one is present then only one.
 * If no aggregation is provided an exception is thrown.
 * @param productAggregation product aggregation as selected by the product aggregation entry user input. Used in group by statements.
 * @param customerAggregation customer aggregation as selected by the customer aggregation entry user input. Used in group by statements.
 * @return raw SQL statement defining the "join" part of the query
 */
protected String getQueryJoinFields(String productAggregation, String customerAggregation) {
    boolean isProductAggregationNone = isDimensionNone(productAggregation)
    boolean isCustomerAggregationNone = isDimensionNone(customerAggregation)

    if (!isProductAggregationNone && !isCustomerAggregationNone) {
        return "T1.ProductId = T2.ProductId AND T1.CustomerId = T2.CustomerId"
    } else if (!isProductAggregationNone) {
        return "T1.ProductId = T2.ProductId"
    } else if (!isCustomerAggregationNone) {
        return "T1.CustomerId = T2.CustomerId"
    }

    api.throwException("Missing aggregation")
    return null
}

/**
 * Retrieves the where statements that are based on the provided aggregations.
 * If both aggregations are present then tables require a where that will look up on both fields, if one is present then only one.
 * If no aggregation is provided an exception is thrown.
 * @param tableAlias which query should be affected by this statement, either T1 or T2
 * @param productAggregation product aggregation as selected by the product aggregation entry user input. Used in group by statements.
 * @param customerAggregation customer aggregation as selected by the customer aggregation entry user input. Used in group by statements.
 * @return where statement that will select either new or lost business based on the provided aggregations
 */
protected String getNewLostBusinessQueryWhere(String tableAlias, String productAggregation, String customerAggregation) {
    boolean isProductAggregationNone = isDimensionNone(productAggregation)
    boolean isCustomerAggregationNone = isDimensionNone(customerAggregation)

    if (!isProductAggregationNone && !isCustomerAggregationNone) {
        return "WHERE ${tableAlias}.ProductId IS NULL AND ${tableAlias}.CustomerId IS NULL"
    } else if (!isProductAggregationNone) {
        return "WHERE ${tableAlias}.ProductId IS NULL"
    } else if (!isCustomerAggregationNone) {
        return "WHERE ${tableAlias}.CustomerId IS NULL"
    }

    api.throwException("Missing aggregation")
    return null
}

/**
 * Checks if the provided aggregation is equal to None or null
 * @param aggregation product/customer aggregation as selected by the product/customer aggregation entry user input.
 * @return true if the dimension is None or null, false otherwise
 */
protected boolean isDimensionNone(String aggregation) {
    String noneDimension = libs.SIP_Dashboards_Commons.ConstConfig.EMPTY_DIMENSION_KEY

    return !aggregation || aggregation == noneDimension
}

String getTimePeriodLabel(Map periodConfigurator, CalendarUtil.TimePeriod period) {
    Script constConfig = libs.SIP_Dashboards_Commons.ConstConfig
    Script dateUtil = libs.SharedLib.DateUtils

    Map periodConfiguratorInputConfig = constConfig.CAUSALITY_PERIOD_CONFIGURATOR_CONFIG.INPUTS
    String periodType = periodConfigurator.getAt(periodConfiguratorInputConfig.PERIOD_TYPE.UNIQUE_KEY)
    String periodLabel
    String dateFormat = "dd/MM/yyyy"

    switch (periodType) {
        case constConfig.YTD_PERIOD_VALUE:
        case constConfig.CUSTOM_PERIOD_VALUE:
            periodLabel = "${dateUtil.getFormattedDate(period.getStartDate(), dateFormat)} - ${dateUtil.getFormattedDate(period.getEndDate(), dateFormat)}"
            break
        case constConfig.WEEK_PERIOD_VALUE:
        case constConfig.MONTH_PERIOD_VALUE:
        case constConfig.QUARTER_PERIOD_VALUE:
            periodLabel = api.getDatamartContext().calendar().getTimePeriodName(period)
            break
    }

    return periodLabel
}

CalendarUtil.TimePeriod getTimePeriodFromConfigurator(Map periodConfigurator) {
    Script constConfig = libs.SIP_Dashboards_Commons.ConstConfig
    Map periodConfiguratorInputConfig = constConfig.CAUSALITY_PERIOD_CONFIGURATOR_CONFIG.INPUTS
    String periodType = periodConfigurator.getAt(periodConfiguratorInputConfig.PERIOD_TYPE.UNIQUE_KEY)
    CalendarUtil.TimePeriod period

    if (periodType == constConfig.CUSTOM_PERIOD_VALUE) {
        period = getCustomTimePeriod(periodConfigurator, periodConfiguratorInputConfig, false)
    } else {
        String yearInputValue = periodConfigurator.getAt(periodConfiguratorInputConfig.YEAR.UNIQUE_KEY)

        if (periodType == constConfig.YTD_PERIOD_VALUE) {
            period = getYTDTimePeriod(yearInputValue)
        } else {
            String periodInputValue = periodConfigurator.getAt(periodConfiguratorInputConfig.PERIOD.UNIQUE_KEY)
            period = getTimePeriod(yearInputValue, periodInputValue)
        }
    }

    return period
}

CalendarUtil.TimePeriod getComparisonTimePeriodFromConfigurator(CalendarUtil.TimePeriod periodTimePeriod, Map periodConfigurator) {
    Script constConfig = libs.SIP_Dashboards_Commons.ConstConfig
    Map periodConfiguratorInputConfig = constConfig.CAUSALITY_PERIOD_CONFIGURATOR_CONFIG.INPUTS
    String periodType = periodConfigurator.getAt(periodConfiguratorInputConfig.PERIOD_TYPE.UNIQUE_KEY)
    CalendarUtil.TimePeriod period

    if (periodType == constConfig.CUSTOM_PERIOD_VALUE) {
        period = getCustomTimePeriod(periodConfigurator, periodConfiguratorInputConfig, true)
    } else {
        String yearInputValue = periodConfigurator.getAt(periodConfiguratorInputConfig.COMPARISON_YEAR.UNIQUE_KEY)

        if (periodType == constConfig.YTD_PERIOD_VALUE) {
            period = getYTDTimePeriod(yearInputValue)
        } else {
            String periodInputValue = periodConfigurator.getAt(periodConfiguratorInputConfig.COMPARISON_PERIOD.UNIQUE_KEY)
            period = getComparisonTimePeriod(yearInputValue, periodInputValue, periodTimePeriod)
        }
    }

    return period
}

protected CalendarUtil.TimePeriod getYTDTimePeriod(String yearInputValue) {
    int currentMonth = Calendar.getInstance().get(Calendar.MONTH) + 1
    int currentDay = Calendar.getInstance().get(Calendar.DAY_OF_MONTH)

    return libs.SIP_Dashboards_Commons.QueryUtils.createCustomTimePeriod("${yearInputValue}-01-01", "${yearInputValue}-${currentMonth}-${currentDay}")
}

protected CalendarUtil.TimePeriod getCustomTimePeriod(Map periodConfigurator, Map periodConfiguratorInputConfig, boolean isComparison) {
    String dateFrom
    String dateTo

    if (isComparison) {
        dateFrom = periodConfigurator.getAt(periodConfiguratorInputConfig.COMPARISON_DATE_FROM.UNIQUE_KEY)
        dateTo = periodConfigurator.getAt(periodConfiguratorInputConfig.COMPARISON_DATE_TO.UNIQUE_KEY)
    } else {
        dateFrom = periodConfigurator.getAt(periodConfiguratorInputConfig.DATE_FROM.UNIQUE_KEY)
        dateTo = periodConfigurator.getAt(periodConfiguratorInputConfig.DATE_TO.UNIQUE_KEY)
    }

    return libs.SIP_Dashboards_Commons.QueryUtils.createCustomTimePeriod(dateFrom, dateTo)
}

protected CalendarUtil.TimePeriod getTimePeriod(String yearEntry, String periodEntry = null) {
    String noneValue = libs.SIP_Dashboards_Commons.ConstConfig.EMPTY_DIMENSION_VALUE
    String yearPeriodCombination = "${yearEntry}-${periodEntry}"
    String periodValue = periodEntry != null && periodEntry != noneValue ? yearPeriodCombination : yearEntry

    return api.getDatamartContext().calendar().getTimePeriod(periodValue)
}

protected CalendarUtil.TimePeriod getComparisonTimePeriod(String yearInput, String comparisonPeriodInput, CalendarUtil.TimePeriod periodOutput) {
    if (yearInput) {
        return getTimePeriod(yearInput, comparisonPeriodInput)
    } else if (comparisonPeriodInput == null) {
        return periodOutput.add(-1)
    } else {
        String prevYear = periodOutput.getStartDate()
                .getAt(java.util.Calendar.YEAR)

        return getTimePeriod(prevYear, comparisonPeriodInput)
    }
}

/**
 * Calculates the Other Effects that are used in the Causality Dashboards.
 * The other effects are calculated by: comparisonPeriod - basePeriod - all other effects.
 * @param basePeriod base period total value of a measure. Can be for example total revenue of T1.
 * @param comparisonPeriod comparison period total value of a measure. Can be for example total revenue of T2.
 * @param figures List defining the list of all the other effects that will be considered in the calculation
 * @return an Other effects value
 */
protected BigDecimal calculateOtherEffects(BigDecimal basePeriod, BigDecimal comparisonPeriod, List<BigDecimal> figures) {
    BigDecimal nonNullFigures = figures?.findAll { it }?.sum() ?: BigDecimal.ZERO

    return comparisonPeriod - basePeriod - nonNullFigures
}

