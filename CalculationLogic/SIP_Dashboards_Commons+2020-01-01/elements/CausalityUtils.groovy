/**
 * Retrieves raw query data for the Revenue Breakdown data dashboard based on the provided input filters.
 * The mappings for the DM fields are retrieved using the SQL_CONFIGURATION.
 * If both aggregation dimensions are selected as None no data is returned.
 * The data retrieved is in the following structure:
 * [
 *   "oldRevenue" : 63579817,
 *   "volumeEffect" : -32334535,
 *   "priceEffect" : 8790089,
 *   "mixEffect" : 2575,
 *   "lostBusiness" : -25856628,
 *   "newBusiness" : 2342,
 *   "newRevenue" : 14183660
 * ]
 * @param productElement product filter as generated by the product group entry user input.
 * @param productAggregation product aggregation as selected by the product aggregation entry user input. Used in group by statements.
 * @param customerElement customer filter as generated by the customer group entry user input.
 * @param customerAggregation customer aggregation as selected by the customer aggregation entry user input. Used in group by statements.
 * @param periodElement element defining the first period, can be a year or a combination of year with a quarter
 * @param comparisonPeriodElement element defining the second period, can be a year or a combination of year with a quarter
 * @param targetCurrencyCode currency code that the dashboards is going to be displayed in. Used for currency conversion.
 * @param sqlConfiguration sql configuration containing data mart fields mapping definition. Stored in SIP_AdvancedConfiguration.
 * @param genericFilter generic filter as generated by the generic filter user input.
 * @return raw query data for the Revenue Breakdown
 */
Map getRevenueBreakdownData(def productElement,
                            String productAggregation,
                            def customerElement,
                            String customerAggregation,
                            def periodElement,
                            def comparisonPeriodElement,
                            String targetCurrencyCode,
                            Map sqlConfiguration,
                            Filter genericFilter) {
    if (isDimensionNone(customerAggregation) && isDimensionNone(productAggregation)) {
        return [:]
    }

    def datamartContext = api.getDatamartContext()

    DatamartContext.Query baseQueryDefinition = getRevenueBaseQuery(datamartContext,
            sqlConfiguration,
            productElement,
            productAggregation,
            customerElement,
            customerAggregation,
            genericFilter)

    DatamartContext.Query comparisonPeriodQuery = getPeriodQuery(datamartContext,
            baseQueryDefinition,
            comparisonPeriodElement,
            sqlConfiguration,
            targetCurrencyCode)

    DatamartContext.Query periodQuery = getPeriodQuery(datamartContext,
            baseQueryDefinition,
            periodElement,
            sqlConfiguration,
            targetCurrencyCode)

    String sqlQuery = getRevenueBreakdownSQLStatement(productAggregation, customerAggregation, sqlConfiguration)

    return datamartContext.executeSqlQuery(sqlQuery, comparisonPeriodQuery, periodQuery)?.getAt(0)
}

/**
 * Retrieves the base query for the Revenue Breakdown, the base query is used for both period queries in the dashboard.
 * Contains all common elements for both period queries, only thing that is missing is the period specific date range.
 * @param dmCtx data mart context object.
 * @param sqlConfiguration sql configuration containing data mart fields mapping definition. Stored in SIP_AdvancedConfiguration.
 * @param productElement product filter as generated by the product group entry user input.
 * @param productAggregation product aggregation as selected by the product aggregation entry user input. Used in group by statements.
 * @param customerElement customer filter as generated by the customer group entry user input.
 * @param customerAggregation customer aggregation as selected by the customer aggregation entry user input. Used in group by statements.
 * @param genericFilter generic filter as generated by the generic filter user input.
 * @return a DatamartContext.Query object describing the base query used in Revenue Breakdown dashboard
 */
protected DatamartContext.Query getRevenueBaseQuery(DatamartContext dmCtx,
                                                    Map sqlConfiguration,
                                                    def productElement,
                                                    String productAggregation,
                                                    def customerElement,
                                                    String customerAggregation,
                                                    Filter genericFilter) {
    def inputConfig = libs.SIP_Dashboards_Commons.ConstConfig.CAUSALITY_DASHBOARD_CONFIG.INPUTS
    def datamart = dmCtx.getDatamart(sqlConfiguration.datamartName)
    def baseQueryDefinition = dmCtx.newQuery(datamart, true)

    addAggregationLevel(baseQueryDefinition,
            productAggregation,
            sqlConfiguration.productId,
            inputConfig.PRODUCT_AGGREGATION.ENTRY_CATEGORY_FIELD)
    addAggregationLevel(baseQueryDefinition,
            customerAggregation,
            sqlConfiguration.customerId,
            inputConfig.CUSTOMER_AGGREGATION.ENTRY_CATEGORY_FIELD)

    baseQueryDefinition.select("SUM(${sqlConfiguration.invoicePrice})", "IP")
    baseQueryDefinition.select("SUM(${sqlConfiguration.quantity})", "V") //
    baseQueryDefinition.select("SUM(${sqlConfiguration.invoicePrice}) / SUM(${sqlConfiguration.quantity})", "IPpu")

    baseQueryDefinition.where(productElement as ProductGroup)
    baseQueryDefinition.where(customerElement as CustomerGroup)
    baseQueryDefinition.where(Filter.isNotNull(sqlConfiguration.invoicePrice),
            Filter.isNotNull(sqlConfiguration.quantity))

    baseQueryDefinition.having(Filter.greaterThan("IP", 0),
            Filter.greaterThan("V", 0))

    if (genericFilter) {
        baseQueryDefinition.where(genericFilter)
    }

    return baseQueryDefinition
}

