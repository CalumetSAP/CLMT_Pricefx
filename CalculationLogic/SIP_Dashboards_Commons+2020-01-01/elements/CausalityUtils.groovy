/**
 * Creates a query object for a given period used in Causality Dashboards.
 * Requires a base query object to extend.
 * Adds only date based data.
 * @param dmCtx data mart context object.
 * @param baseQueryDefinition definition of the query as defined by the Dashboard Library. Generated by getMarginBaseQuery or getRevenueBaseQuery
 * @param dateElement either Period or ComparisonPeriod elements, used to create date filters
 * @param sqlConfiguration sql configuration containing data mart fields mapping definition. Stored in SIP_AdvancedConfiguration.
 * @param targetCurrencyCode currency code that the dashboards is going to be displayed in. Used for currency conversion.
 * @return query object to be used as main data fetch tool for all Causality Dashboards.
 */
protected DatamartContext.Query getPeriodQuery(DatamartContext dmCtx,
                                               DatamartContext.Query baseQueryDef,
                                               def dateElement,
                                               Map sqlConfiguration,
                                               String targetCurrencyCode) {
    def query = dmCtx.newQuery(baseQueryDef)
    def periodStartDate = dateElement.getStartDate()
    def periodEndDate = dateElement.getEndDate()
    query.where(Filter.lessOrEqual(sqlConfiguration.pricingDate, periodEndDate),
            Filter.greaterOrEqual(sqlConfiguration.pricingDate, periodStartDate))
    query.setOptions([currency: targetCurrencyCode])

    return query
}

/**
 * Appends the base query definition with aggregation field if required.
 * Selection of None dimension prevents the addition.
 * @param baseQueryDefinition definition of the query as defined by the Dashboard Library. Generated by getMarginBaseQuery or getRevenueBaseQuery
 * @param aggregation product/customer aggregation as selected by the product/customer aggregation entry user input.
 * @param sqlField field that acts as a fallback in case the aggregation is not provided.
 * @param sqlAlias alias for the aggregation field that will be returned from the query
 */
protected void addAggregationLevel(def baseQueryDefinition, String aggregation, String sqlField, String sqlAlias) {
    if (!aggregation || aggregation == libs.SIP_Dashboards_Commons.ConstConfig.EMPTY_DIMENSION_KEY) {
        return
    }

    String aggregationField = getAggregationLevelSQLField(aggregation, sqlField)
    baseQueryDefinition.select(aggregationField, sqlAlias)
}

/**
 * Retrieves which of the provided entries should be used.
 * If the aggregation input is not provided or it's equal to sqlField the sqlField is returned,
 * otherwise the aggregation is used.
 * @param aggregation product/customer aggregation as selected by the product/customer aggregation entry user input.
 * @param sqlField field that acts as a fallback in case the aggregation is not provided.
 * @return either aggregation or sqlField depending on the data availability
 */
protected String getAggregationLevelSQLField(String aggregation, String sqlField) {
    return !aggregation || (aggregation == sqlField) ? sqlField : aggregation

}

