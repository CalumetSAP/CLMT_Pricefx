/**
 * Retrieves the legacy SQL statement for the Revenue Breakdown Dashboard
 * The SQL returns all main data required by the dashboard.
 * The query requires two period queries named respectively T1 and T2.
 * @param joinFields fields used to join T1 and T2.
 * @param lostBusinessWhere sql WHERE clause to retrieve lost business.
 * @param newBusinessWhere sql WHERE clause to retrieve new business.
 * @return raw SQL statement returning all data required for Revenue Dashboard
 */
protected String getLegacyRevenueBreakdownSQLStatement(String joinFields, String lostBusinessWhere, String newBusinessWhere) {
    return """SELECT
                ROUND(PERIOD1.IP) AS 'oldRevenue',
                ROUND(COMMON_DATA.VolumeEffect) AS 'volumeEffect',
                ROUND(COMMON_DATA.PriceEffect) AS 'priceEffect',
                ROUND(COMMON_DATA.MixEffect) AS 'mixEffect',
                ROUND(-LOST_DATA.IP) AS 'lostBusiness',
                ROUND(PERIOD2.IP - PERIOD1.IP - (COALESCE(-LOST_DATA.IP, 0) + COALESCE(COMMON_DATA.PriceEffect, 0) + COALESCE(COMMON_DATA.VolumeEffect, 0) + COALESCE(COMMON_DATA.MixEffect, 0) + COALESCE(NEW_DATA.IP, 0))) AS 'otherEffects',
                ROUND(NEW_DATA.IP) AS 'newBusiness',
                ROUND(PERIOD2.IP) AS 'newRevenue'
                FROM (SELECT
                      SUM((T2.IPpu - T1.IPpu) * T2.V) AS PriceEffect,
                      (SUM(T2.V)-SUM(T1.V)) * (SUM(T1.IP) / SUM(T1.V)) AS VolumeEffect,
                      SUM(T1.IPpu * (T2.V/TOTAL.T2V - T1.V/TOTAL.T1V)) * SUM(T2.V) AS MixEffect
                      FROM T2
                          INNER JOIN T1 ON ${joinFields},
                        (SELECT SUM(T1.V) AS T1V, SUM(T2.V) AS T2V
                          FROM T2 INNER JOIN T1 ON
                            ${joinFields}) TOTAL) COMMON_DATA,
                    (SELECT SUM(T1.IP) AS ip FROM T1
                        LEFT OUTER JOIN T2 ON ${joinFields} ${lostBusinessWhere}) LOST_DATA,
                    (SELECT SUM(T2.IP) AS ip FROM T1
                        RIGHT OUTER JOIN T2 ON ${joinFields} ${newBusinessWhere}) NEW_DATA,
                    (SELECT SUM(T1.IP) IP FROM T1) PERIOD1,
                    (SELECT SUM(T2.IP) IP FROM T2) PERIOD2
                                 """
}

/**
 * Retrieves the standard SQL statement for the Revenue Breakdown Dashboard
 * The SQL returns all main data required by the dashboard.
 * The query requires two period queries named respectively T1 and T2.
 * @param joinFields fields used to join T1 and T2.
 * @param lostBusinessWhere sql WHERE clause to retrieve lost business.
 * @param newBusinessWhere sql WHERE clause to retrieve new business.
 * @return raw SQL statement returning all data required for Revenue Dashboard
 */
protected String getStandardRevenueBreakdownSQLStatement(String joinFields, String lostBusinessWhere, String newBusinessWhere) {
    return """SELECT
                ROUND(PERIOD1.IP) AS 'oldRevenue',
                ROUND(COMMON_DATA.VolumeEffect) AS 'volumeEffect',
                ROUND(COMMON_DATA.PriceEffect) AS 'priceEffect',
                ROUND(COMMON_DATA.MixEffect) AS 'mixEffect',
                ROUND(PERIOD2.IP - PERIOD1.IP - (COALESCE(-LOST_DATA.IP, 0) + COALESCE(COMMON_DATA.PriceEffect, 0) + COALESCE(COMMON_DATA.VolumeEffect, 0) + COALESCE(COMMON_DATA.MixEffect, 0) + COALESCE(NEW_DATA.IP, 0))) AS 'otherEffects',
                ROUND(-LOST_DATA.IP) AS 'lostBusiness',
                ROUND(NEW_DATA.IP) AS 'newBusiness',
                ROUND(PERIOD2.IP) AS 'newRevenue'
                FROM (SELECT
                        SUM((T2.V + T1.V) / 2) * SUM((T2.V / TOTAL.T2V + T1.V / TOTAL.T1V) / 2 * (T2.IPpu - T1.IPpu)) AS PriceEffect,
                        SUM(T2.V - T1.V) * SUM((T2.V / TOTAL.T2V * T2.IPpu + T1.V / TOTAL.T1V * T1.IPpu) / 2) AS VolumeEffect,
                        SUM((T2.V + T1.V) / 2) * SUM((T2.V / TOTAL.T2V - T1.V / TOTAL.T1V) * (T2.IPpu + T1.IPpu) / 2) AS MixEffect
                      FROM T2
                          INNER JOIN T1 ON ${joinFields},
                        (SELECT SUM(T1.V) AS T1V, SUM(T2.V) AS T2V
                          FROM T2 INNER JOIN T1 ON
                            ${joinFields}) TOTAL) COMMON_DATA,
                    (SELECT SUM(T1.IP) AS ip FROM T1
                        LEFT OUTER JOIN T2 ON ${joinFields} ${lostBusinessWhere}) LOST_DATA,
                    (SELECT SUM(T2.IP) AS ip FROM T1
                        RIGHT OUTER JOIN T2 ON ${joinFields} ${newBusinessWhere}) NEW_DATA,
                    (SELECT SUM(T1.IP) IP FROM T1) PERIOD1,
                    (SELECT SUM(T2.IP) IP FROM T2) PERIOD2
                                 """
}

/**
 * Retrieves the main SQL statement for the Margin Breakdown Dashboard for the Net calculation model.
 * The SQL returns all main data required by the dashboard.
 * The query requires two period queries named respectively T1 and T2.
 * @param productAggregation product aggregation as selected by the product aggregation entry user input. Used in group by statements.
 * @param customerAggregation customer aggregation as selected by the customer aggregation entry user input. Used in group by statements.
 * @return raw SQL statement returning all data required for Margin Dashboard
 */
protected String getNetMarginBreakdownSQLStatement(String productAggregation, String customerAggregation) {
    String joinFields = getQueryJoinFields(productAggregation, customerAggregation)
    String lostBusinessWhere = getNewLostBusinessQueryWhere("T2", productAggregation, customerAggregation)
    String newBusinessWhere = getNewLostBusinessQueryWhere("T1", productAggregation, customerAggregation)

    return """SELECT
                ROUND(PERIOD1.Margin) AS 'period1Margin',
                ROUND(COMMON_DATA.VolumeEffect) AS 'volumeEffect',
                ROUND(COMMON_DATA.PriceEffect) AS 'priceEffect',
                ROUND(-COMMON_DATA.CostEffect) AS 'costEffect',
                ROUND(COMMON_DATA.MixEffect) AS 'mixEffect',
                ROUND(-LOST_DATA.Margin) AS 'lostBusiness',
                ROUND(NEW_DATA.Margin) AS 'newBusiness',
                ROUND(PERIOD2.Margin) AS 'period2Margin',
                ROUND(PERIOD2.Margin - PERIOD1.Margin - (COALESCE(-LOST_DATA.Margin, 0) + COALESCE(COMMON_DATA.PriceEffect, 0) + COALESCE(COMMON_DATA.VolumeEffect, 0) + COALESCE(-COMMON_DATA.CostEffect, 0) + COALESCE(COMMON_DATA.MixEffect, 0) + COALESCE(NEW_DATA.Margin, 0))) AS 'otherEffects'
                FROM (SELECT
                          SUM((T2.InvoicePricePerUnit - T1.InvoicePricePerUnit) * T1.Volume) AS PriceEffect,
                          SUM((T2.Volume - T1.Volume) * T1.MarginPerUnit) AS VolumeEffect,
                          SUM((T2.CostPerUnit - T1.CostPerUnit) * T1.Volume) AS CostEffect,
                          SUM((T2.Volume/TOTAL.T2Volume - T1.Volume/TOTAL.T1Volume) * (T1.MarginPerUnit - (T1.Margin/TOTAL.T1Volume))) * SUM(T1.Volume) AS MixEffect
                          FROM T2
                             INNER JOIN T1 ON ${joinFields},
                             (SELECT SUM(T1.Volume) AS T1Volume, SUM(T2.Volume) AS T2Volume
                              FROM T2 INNER JOIN T1 ON
                               ${joinFields}) TOTAL) COMMON_DATA,
                      (SELECT SUM(T1.Margin) AS Margin FROM T1
                        LEFT OUTER JOIN T2 ON ${joinFields} ${lostBusinessWhere}) LOST_DATA,
                      (SELECT SUM(T2.Margin) AS Margin FROM T1
                       RIGHT OUTER JOIN T2 ON ${joinFields} ${newBusinessWhere}) NEW_DATA,
                      (SELECT SUM(T1.Margin) Margin FROM T1) PERIOD1,
                      (SELECT SUM(T2.Margin) Margin FROM T2) PERIOD2
                                 """
}

/**
 * Retrieves the main SQL statement for the Margin Breakdown Dashboard for the legacy mode.
 * The SQL returns all main data required by the dashboard.
 * The query requires two period queries named respectively T1 and T2.
 * @param model calculation model selected by the user in the Model user input. Available models defined in libs.SIP_Dashboards_Commons.ConstConfig.MARGIN_BREAKDOWN_DASHBOARD_CONFIG
 * @param productAggregation product aggregation as selected by the product aggregation entry user input. Used in group by statements.
 * @param customerAggregation customer aggregation as selected by the customer aggregation entry user input. Used in group by statements.
 * @return raw SQL statement returning all data required for Margin Dashboard
 */
protected String getLegacyMarginBreakdownSQLStatement(String model, String productAggregation, String customerAggregation) {
    def commonsConstConfig = libs.SIP_Dashboards_Commons.ConstConfig
    String sqlStatement

    switch (model) {
        case commonsConstConfig.MARGIN_BREAKDOWN_GROSS_CHART_KEY:
            sqlStatement = getGrossMarginBreakdownSQLStatement(productAggregation, customerAggregation)
            break
        case commonsConstConfig.MARGIN_BREAKDOWN_AVERAGES_CHART_KEY:
            sqlStatement = getAveragesMarginBreakdownSQLStatement(productAggregation, customerAggregation)
            break
        case commonsConstConfig.MARGIN_BREAKDOWN_MOST_USED_CHART_KEY:
            sqlStatement = getMostUsedMarginBreakdownSQLStatement(productAggregation, customerAggregation)
            break
        default:
            sqlStatement = getNetMarginBreakdownSQLStatement(productAggregation, customerAggregation)
            break
    }

    return sqlStatement
}

